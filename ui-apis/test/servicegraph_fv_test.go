func executeHTTPRequest[TReq any, TResp any](reqObj TReq, path string, handler http.Handler, testUser *user.DefaultInfo, clusterName string) (*TResp, error) {
	body, err := json.Marshal(reqObj)
	if err != nil {
		return nil, err
	}

	req, err := http.NewRequest("POST", path, bytes.NewReader(body))
	if err != nil {
		return nil, err
	}

	req.Header.Set("Content-Type", "application/json")
	req = req.WithContext(request.WithUser(req.Context(), testUser))
	req = req.WithContext(auth.NewContextWithReviewResource(
		req.Context(),
		esauth.CreateLMAResourceAttributes(clusterName, "flows"),
	))

	rr := httptest.NewRecorder()
	handler.ServeHTTP(rr, req)

	if rr.Code != http.StatusOK {
		return nil, fmt.Errorf("request failed: %v", rr.Body)
	}

	var resp TResp
	err = json.NewDecoder(rr.Body).Decode(&resp)
	if err != nil {
		return nil, err
	}

	return &resp, nil
}

