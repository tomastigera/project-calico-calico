// createTestUser creates a test user with RBAC permissions. See testUser for details.
func createTestUser(k8sClient *kubernetes.Clientset, config testUser) (*user.DefaultInfo, func()) {
	ctx := context.Background()
	var cleanupFuncs []func()

	// Create ServiceAccount in "default" namespace
	sa := &corev1.ServiceAccount{
		ObjectMeta: metav1.ObjectMeta{
			Name:      config.Username,
			Namespace: "default",
		},
	}
	_, err := k8sClient.CoreV1().ServiceAccounts("default").Create(ctx, sa, metav1.CreateOptions{})
	cleanupFuncs = append(cleanupFuncs, func() {
		_ = k8sClient.CoreV1().ServiceAccounts("default").Delete(context.Background(), config.Username, metav1.DeleteOptions{})
	})
	Expect(err).NotTo(HaveOccurred(), "Failed to create ServiceAccount")

	// Define the subject for all role bindings
	subject := []rbacv1.Subject{{
		Kind:      "ServiceAccount",
		Name:      config.Username,
		Namespace: "default",
	}}

	// Determine RBAC scope based on namespaces parameter
	if len(config.Namespaces) == 0 {
		// Select and potentially create the ClusterRole
		var clusterRoleName string
		if len(config.PolicyRules) == 0 {
			// Use existing cluster-admin ClusterRole
			clusterRoleName = "cluster-admin"
		} else {
			// Create custom ClusterRole with provided rules
			clusterRoleName = config.Username + "-role"
			cr := &rbacv1.ClusterRole{
				ObjectMeta: metav1.ObjectMeta{
					Name: clusterRoleName,
				},
				Rules: config.PolicyRules,
			}
			_, err = k8sClient.RbacV1().ClusterRoles().Create(ctx, cr, metav1.CreateOptions{})
			Expect(err).NotTo(HaveOccurred(), "Failed to create ClusterRole")

			// Add ClusterRole to cleanup
			cleanupFuncs = append(cleanupFuncs, func() {
				_ = k8sClient.RbacV1().ClusterRoles().Delete(context.Background(), clusterRoleName, metav1.DeleteOptions{})
			})
		}

		// Create ClusterRoleBinding to the selected ClusterRole
		crb := &rbacv1.ClusterRoleBinding{
			ObjectMeta: metav1.ObjectMeta{
				Name: config.Username + "-binding",
			},
			Subjects: subject,
			RoleRef: rbacv1.RoleRef{
				APIGroup: "rbac.authorization.k8s.io",
				Kind:     "ClusterRole",
				Name:     clusterRoleName,
			},
		}
		_, err = k8sClient.RbacV1().ClusterRoleBindings().Create(ctx, crb, metav1.CreateOptions{})
		Expect(err).NotTo(HaveOccurred(), "Failed to create ClusterRoleBinding")

		cleanupFuncs = append(cleanupFuncs, func() {
			_ = k8sClient.RbacV1().ClusterRoleBindings().Delete(context.Background(), config.Username+"-binding", metav1.DeleteOptions{})
		})

	} else {
		// Namespace-scoped permissions
		Expect(config.PolicyRules).NotTo(BeEmpty(), "Cannot create namespace-scoped permissions without rules")

		// Create Role + RoleBinding in each namespace
		for _, ns := range config.Namespaces {
			roleName := config.Username + "-role"

			role := &rbacv1.Role{
				ObjectMeta: metav1.ObjectMeta{
					Name:      roleName,
					Namespace: ns,
				},
				Rules: config.PolicyRules,
			}
			_, err = k8sClient.RbacV1().Roles(ns).Create(ctx, role, metav1.CreateOptions{})
			Expect(err).NotTo(HaveOccurred(), "Failed to create Role in namespace "+ns)

			rb := &rbacv1.RoleBinding{
				ObjectMeta: metav1.ObjectMeta{
					Name:      config.Username + "-binding",
					Namespace: ns,
				},
				Subjects: subject,
				RoleRef: rbacv1.RoleRef{
					APIGroup: "rbac.authorization.k8s.io",
					Kind:     "Role",
					Name:     roleName,
				},
			}
			_, err = k8sClient.RbacV1().RoleBindings(ns).Create(ctx, rb, metav1.CreateOptions{})
			Expect(err).NotTo(HaveOccurred(), "Failed to create RoleBinding in namespace "+ns)

			namespace := ns
			cleanupFuncs = append(cleanupFuncs, func() {
				_ = k8sClient.RbacV1().RoleBindings(namespace).Delete(context.Background(), config.Username+"-binding", metav1.DeleteOptions{})
				_ = k8sClient.RbacV1().Roles(namespace).Delete(context.Background(), roleName, metav1.DeleteOptions{})
			})
		}
	}

	// Finally, create ClusterRole for authorizationreviews (required for RBAC filtering to work)
	// This is independent of the user's configured permissions.
	authzRoleName := config.Username + "-authz-role"
	authzRole := &rbacv1.ClusterRole{
		ObjectMeta: metav1.ObjectMeta{
			Name: authzRoleName,
		},
		Rules: []rbacv1.PolicyRule{
			{
				APIGroups: []string{"projectcalico.org"},
				Resources: []string{"authorizationreviews"},
				Verbs:     []string{"create"},
			},
		},
	}
	_, err = k8sClient.RbacV1().ClusterRoles().Create(ctx, authzRole, metav1.CreateOptions{})
	Expect(err).NotTo(HaveOccurred(), "Failed to create authorizationreviews ClusterRole")
	cleanupFuncs = append(cleanupFuncs, func() {
		_ = k8sClient.RbacV1().ClusterRoles().Delete(context.Background(), authzRoleName, metav1.DeleteOptions{})
	})

	// Create ClusterRoleBinding for authorizationreviews
	authzBindingName := config.Username + "-authz-binding"
	authzBinding := &rbacv1.ClusterRoleBinding{
		ObjectMeta: metav1.ObjectMeta{
			Name: authzBindingName,
		},
		Subjects: subject,
		RoleRef: rbacv1.RoleRef{
			APIGroup: "rbac.authorization.k8s.io",
			Kind:     "ClusterRole",
			Name:     authzRoleName,
		},
	}
	_, err = k8sClient.RbacV1().ClusterRoleBindings().Create(ctx, authzBinding, metav1.CreateOptions{})
	Expect(err).NotTo(HaveOccurred(), "Failed to create authorizationreviews ClusterRoleBinding")
	cleanupFuncs = append(cleanupFuncs, func() {
		_ = k8sClient.RbacV1().ClusterRoleBindings().Delete(context.Background(), authzBindingName, metav1.DeleteOptions{})
	})

	teardown := func() {
		for i := len(cleanupFuncs) - 1; i >= 0; i-- {
			cleanupFuncs[i]()
		}
	}

	return &user.DefaultInfo{
		Name:   "system:serviceaccount:default:" + config.Username,
		UID:    "",
		Groups: []string{"system:serviceaccounts", "system:serviceaccounts:default"},
	}, teardown
}

